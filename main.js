function splitString(stringToSplit) {
    //console.log(stringToSplit);
        return stringToSplit.split(" ").map(
            function secondSplitter(x){
                return x.split(",");
        });
}
function loopThrough(myarray) {
    var obj = {
        valarr:[],
        names: []
    };

    for (var content in myarray) {
        var cGroups = [];
        var a;

        for (var attr in myarray[content].content){
            a = myarray[content].content;
            var b;

            for (var points in a[attr].attrs){
                b = a[attr].attrs.points;
                var n = myarray[content].id;
            }

            var x = splitString(b);
            cGroups.push(x);
        }
        // push organized and split values into vallarr
        obj.valarr.push(cGroups);
        obj.names.push(n);
    }
    return obj;
}


var params = {
    autostart: true,
    width: '100%',
    height: window.innerHeight - window.innerHeight*0.1 + 'px'
};

$(document).ready(function() {
    var elem = document.body;
    elem.style.background = "rgb(35, 37, 59)";
    var two = new Two(params).appendTo(elem);

    var test = loopThrough(cons_cont.list);
    var star = pickStarPos(cons_star.list);
    var constLines = makeConst(test.valarr);

    var pS = parseStar(star.valarr);
    var cL = drawConstLines(constLines, test.names, "white");

    var all = two.makeGroup(pS, cL);
    //all.scale = 3;
    //all.rotation = Math.PI/4;

    two.update();

    function makeConst(data) {
        var line = [];
        for (var i = 0; i < data.length; i++) {
            var groups = [];
            for (var j = 0; j < data[i].length; j++ ){
                var pnts = [];
                for (var k = 0; k < data[i][j].length; k++){
                        var ptx = data[i][j][k][0], pty = data[i][j][k][1];
                        var anchor = new Two.Anchor(ptx, pty);
                        pnts.push(anchor);
                }
                groups.push(pnts);
            }
            line.push(groups);
        }
        return line;
    }
    function drawConstLines(data, name, sColor){
        var polyGroups = [];
        for (var i = 0; i < data.length; i++) {
            var points;
            var lines = [];
            for (var j = 0; j < data[i].length; j++ ){
                points = data[i][j];
                //console.log(points);
                var ln = two.makePolygon(points, !closed);
                ln.noFill();
                ln.stroke = sColor;
                lines.push(ln);
            }
            var g = two.makeGroup(lines);
            g.id = name[i];
            polyGroups.push(g);
        }
        var x = two.makeGroup(polyGroups);
        return x;
    }
    function pickStarPos(myarray) {
        var obj = {
            valarr:[],
            names: []
        };

        for (var content in myarray) {
            var cGroups = [];
            var cRad = [];
            var a;

            for (var attr in myarray[content].content){
                a = myarray[content].content;
                var bx, by, bRad;

                for (var points in myarray[content].content[attr].attrs){

                    //console.log(myarray[content].content[attr].attrs.cx);
                    bx = myarray[content].content[attr].attrs.cx;
                    by = myarray[content].content[attr].attrs.cy;
                    bRad = myarray[content].content[attr].attrs.r;
                    var n = myarray[content].id;
                }
                var circ = {
                    x: bx,
                    y: by,
                    r: bRad
                };

                cGroups.push(circ);
            }
            // push organized and split values into vallarr
            obj.valarr.push(cGroups);

            obj.names.push(n);
        }
        return obj;
    }
    function parseStar(myarray){
        var star = [];
        for (var i = 0; i < myarray.length; i++){
            var groups = [];
            for (var j = 0; j < myarray[i].length; j++){
                var ptx = myarray[i][j].x, pty = myarray[i][j].y, r = myarray[i][j].r;
                    var circle = two.makeCircle(ptx, pty, r);
                    circle.fill = "white";
                    circle.noStroke();
                    groups.push(circle);
            }
            var g = two.makeGroup(groups);
            star.push(g);
        }
        var s = two.makeGroup(star);
        return s;
    }

    /********************
     * SCROLLWHEEL ZOOM *
     ********************/
    var zui = new ZUI(two);
    zui.addLimits(0.06, 8);

    var $stage = $(two.renderer.domElement);
    $stage.bind('mousewheel', function(event) {
        var e = event.originalEvent;
        e.stopPropagation();
        e.preventDefault();
        var dy = e.wheelDeltaY / 1000;
        zui.zoomBy(dy, e.clientX, e.clientY);
    });


});

